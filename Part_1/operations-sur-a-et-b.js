// Déclaration des variables
let a = 5;
let b = 10;

// Calcul de la somme
let somme = a + b;
console.log(`La somme de a et b est : ${somme}`);

// Calcul du produit
let produit = a * b;
console.log(`Le produit de a et b est : ${produit}`);

// Vérification si a est inférieur à b
let estInferieur = a < b;
console.log(`a est inférieur à b : ${estInferieur}`);
